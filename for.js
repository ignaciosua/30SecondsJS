//Let's create an Array
let myArray = [1,2,3,4,5];

// myArray.length will return the current length of the array
for (let index = 0; index < myArray.length; index++) {
    const element = myArray[index];
    console.log(element);
}
// This for cicle will start runing with index at 0
// then is going to check second statement
// where index = 0 lower than myArray.lenght = 5
// Now is going to execute instructions in the for cicle 
// And after is finished it will run index++ to increase index by 1


