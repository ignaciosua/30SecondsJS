//How to create a switch statement in javascript
//Remember we are using Code Runner Extension in VScode to Execute JS

//Declare your variables!

let valueToBeCompared = 1;

switch (valueToBeCompared) {
    case 1:
        console.log("Case one!");
    break;
    case 2:
        console.log("Case Two!");
    break;
    default:
        console.log("if nothing matches will always execute this!");
    break;
}

